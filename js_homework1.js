// 1. 'number' + 3 + 3
// 'number' + 3 → 'number3' (рядок + число → конкатенація)
// 'number3' + 3 → 'number33' (рядок + число → конкатенація)
'number' + 3 + 3; // 'number33'

// 2. null + 3
// null → 0 (при арифметичних операціях)
// 0 + 3 → 3
null + 3; // 3

// 3. 5 && "qwerty"
// 5 → truthy, тому переходимо до наступного операнда
// "qwerty" → truthy, повертаємо "qwerty"
5 && "qwerty"; // "qwerty"

// 4. +'40' + +'2' + "hillel"
// +'40' → 40 (унарний + перетворює рядок у число)
// +'2' → 2 (унарний + перетворює рядок у число)
// 40 + 2 → 42 (числове додавання)
// 42 + "hillel" → '42hillel' (число + рядок → конкатенація)
+'40' + +'2' + "hillel"; // '42hillel'

// 5. '10' - 5 === 6
// '10' - 5 → 10 - 5 → 5 (рядок перетворюється на число)
// 5 === 6 → false
'10' - 5 === 6; // false

// 6. true + false
// true → 1, false → 0 (перетворення у числа)
// 1 + 0 → 1
true + false; // 1

// 7. '4px' - 3
// '4px' → NaN (неможливо перетворити в число)
// NaN - 3 → NaN
'4px' - 3; // NaN

// 8. '4' - 3
// '4' → 4 (рядок перетворюється в число)
// 4 - 3 → 1
'4' - 3; // 1

// 9. '6' + 3 ** 0
// 3 ** 0 → 1 (експонента, 3 у степені 0 = 1)
// '6' + 1 → '61' (рядок + число → конкатенація)
'6' + 3 ** 0; // '61'

// 10. 12 / '6'
// '6' → 6 (рядок перетворюється у число)
// 12 / 6 → 2
12 / '6'; // 2

// 11. '10' + (5 === 6)
// 5 === 6 → false
// '10' + false → '10false' (рядок + логічне → конкатенація)
'10' + (5 === 6); // '10false'

// 12. null == ''
// null не дорівнює '' при порівнянні через ==
// Результат: false
null == ''; // false

// 13. 3 ** (9 / 3)
// 9 / 3 → 3
// 3 ** 3 → 27 (3 у степені 3)
3 ** (9 / 3); // 27

// 14. !!'false' == !!'true'
// 'false' і 'true' → truthy значення, !! перетворює їх у true
// true == true → true
!!'false' == !!'true'; // true

// 15. 0 || '0' && 1
// 0 || '0' → '0' (перший truthy значення)
// '0' && 1 → 1 (останнє truthy значення)
// Результат: 1
0 || '0' && 1; // 1

// 16. (+null == false) < 1
// +null → 0 (null перетворюється в число)
// 0 == false → true
// true < 1 → true
(+null == false) < 1; // true

// 17. false && true || true
// false && true → false
// false || true → true
false && true || true; // true

// 18. false && (false || true)
// false || true → true
// false && true → false
false && (false || true); // false

// 19. (+null == false) < 1 ** 5
// +null → 0
// 0 == false → true
// 1 ** 5 → 1 (експонента)
// true < 1 → true
(+null == false) < 1 ** 5; // true
